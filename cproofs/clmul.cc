uint32_t gray_fwd(uint32_t x)
{
        x ^= x >> 1;
        return x;
}

uint32_t gray_inv(uint32_t x)
{
	x = rv32b::clmulr(x, 0xffffffff);
        return x;
}

extern "C" void check_gray_inv(uint32_t x)
{
	uint32_t y = gray_fwd(x);
	uint32_t z = gray_inv(y);
	assert(x == z);
}

